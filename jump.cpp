const int MAX_VALUE = 99999;

//从form 到 to 最小跳跃次数
int findMinJump(int from , int to , vector<vector<int>> &memo){
    if(memo[from][to] != MAX_VALUE){
        return memo[from][to];
    }

    //find min jump
    int minJump = memo[from][to];
    for(int i = from + 1; i < to ; i++){
        int leftJump = findMinJump(from , i , memo);
        int rightJump = findMinJump(i , to , memo);

        int jumCount = leftJump + rightJump;
        if(jumCount < minJump){
            minJump = jumCount;
        }
    }//end for 
    if(minJump == MAX_VALUE){
        minJump = MAX_VALUE - 1;
    }
    //memo[from][to] = minJump;
    for(int i = from ;i <= to ;i++){
        memo[from][to] = minJump;
    }
    return minJump;
}

int jump(vector<int>& nums) {
    if(nums.size() <= 1){
        return 0;
    }

    const int size = nums.size();
    vector<vector<int>> memo;
    //init
    for(int i = 0 ; i < size ;i++){
        vector<int> sub;
        for(int j = 0 ;j < size ;j++){
            sub.push_back(MAX_VALUE);
        }//end for j
        memo.push_back(sub);
    }

    for(int i = 0 ; i < size ;i++){
        for(int j = i + 1 ; j < size ; j++){
            if(j - i <= nums[i]){
                memo[i][j] = 1;
            }
        }//end for j
    }//end for i
    // printArray(memo);

    int result = findMinJump(0 , size - 1 , memo);
    // cout << "result = " << result<< endl;
    return result;
}


